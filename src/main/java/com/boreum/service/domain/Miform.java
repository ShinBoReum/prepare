package com.boreum.service.domain;

public class Miform {
	private String testinsp;
	private String testfiled;
	private String filedcode;
	private String formattype;
	private String coltype;
	private String colcode;
	private String displyorder;
	private String keyword;
	private String condition;
	
	public String getTestinsp() {
		return testinsp;
	}
	public void setTestinsp(String testinsp) {
		this.testinsp = testinsp;
	}
	public String getTestfiled() {
		return testfiled;
	}
	public void setTestfiled(String testfiled) {
		this.testfiled = testfiled;
	}
	public String getFiledcode() {
		return filedcode;
	}
	public void setFiledcode(String filedcode) {
		this.filedcode = filedcode;
	}
	public String getFormattype() {
		return formattype;
	}
	public void setFormattype(String formattype) {
		this.formattype = formattype;
	}
	public String getColtype() {
		return coltype;
	}
	public void setColtype(String coltype) {
		this.coltype = coltype;
	}
	public String getColcode() {
		return colcode;
	}
	public void setColcode(String colcode) {
		this.colcode = colcode;
	}
	public String getDisplyorder() {
		return displyorder;
	}
	public void setDisplyorder(String displyorder) {
		this.displyorder = displyorder;
	}
	public String getKeyword() {
		return keyword;
	}
	public void setKeyword(String keyword) {
		this.keyword = keyword;
	}
	public String getCondition() {
		return condition;
	}
	public void setCondition(String condition) {
		this.condition = condition;
	}
	@Override
	public String toString() {
		return "Miform [testinsp=" + testinsp + ", testfiled=" + testfiled + ", filedcode=" + filedcode
				+ ", formattype=" + formattype + ", coltype=" + coltype + ", colcode=" + colcode + ", displyorder="
				+ displyorder + ", keyword=" + keyword + ", condition=" + condition + "]";
	}
	
		
	
}
